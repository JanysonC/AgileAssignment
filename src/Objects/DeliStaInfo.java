
package Objects;

public class DeliStaInfo {
     
    private String deliName;
    private String deliIC;
    private String gender;
    private String deliContact;
    private String email;
    private String address;
    private String status;
    private String deliEm;
    private int deliID;
    
    public DeliStaInfo(){
        
    }

    public String getDeliName() {
        return deliName;
    }

    public void setDeliName(String deliName) {
        this.deliName = deliName;
    }

    public String getDeliIC() {
        return deliIC;
    }

    public void setDeliIC(String deliIC) {
        this.deliIC = deliIC;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDeliContact() {
        return deliContact;
    }

    public void setDeliContact(String deliContact) {
        this.deliContact = deliContact;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDeliEm() {
        return deliEm;
    }

    public void setDeliEm(String deliEm) {
        this.deliEm = deliEm;
    }

    public int getDeliID() {
        return deliID;
    }

    public void setDeliID(int deliID) {
        this.deliID = deliID;
    }
    
}
